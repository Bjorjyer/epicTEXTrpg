import random
import sys
sys.stdout.reconfigure(encoding='utf-8') # для поддержки русских символов в консоли

character1 = { # персонаж 1
    "Здоровье": 100,
    "Имя": "Hero",
    "Уровень": random.randint(10, 20),
    "Мана": 20,
    "Снаряжение": {
    "оружие": "меч",
    "броня": "кожаный доспех",
    "зелья": ["зелье здоровья", "зелье маны"]
}
}

print(character1["Имя"])
print(f"Здоровье: {character1['Здоровье']}") 
print(f"Мана: {character1['Мана']}")
print(f"Уровень: {character1['Уровень']}")
print(f"Снаряжение: {character1['Снаряжение']}")

character2 = { # персонаж 2
    "Здоровье": 80,
    "Имя": "Enemy",
    "Уровень": random.randint(10, 20),
    "Мана": 50,
    "Снаряжение": {
        "оружие": "кинжал",
        "броня": "магический плащ",
        "зелья": ["зелье здоровья", "зелье маны"]
    }
}

Характеристики_оружия = {
    "меч": {"урон": 15},
    "кинжал": {"урон": 10},
    "магический посох": {"урон": 20}
}
print(character2["Имя"])
print(f"Здоровье: {character2['Здоровье']}") 
print(f"Мана: {character2['Мана']}")
print(f"Уровень: {character2['Уровень']}")
print(f"Снаряжение: {character2['Снаряжение']}")

def подсчитать_урон(мин, макс):
    return(random.randint(мин, макс))
def ударить_противника(атакующий, защищающийся):
    базовый_урон = Характеристики_оружия[атакующий["Снаряжение"]["оружие"]]["урон"]
    случайный_урон = подсчитать_урон(-5, 5)
    общий_урон = базовый_урон + случайный_урон
    return общий_урон
def использовать_зелье(персонаж):
    if персонаж["Здоровье"] < 30 and "зелье" in персонаж["Снаряжение"]["зелья"]:
        персонаж["Здоровье"] += 20
        персонаж["Снаряжение"]["зелья"].remove("зелье")
        print(f"{персонаж['Имя']} использует зелье! Здоровье восстановлено до: {персонаж['Здоровье']}")
def использовать_заклинание_лечения(персонаж):
    if персонаж["Мана"] >= 20 and персонаж["Здоровье"] < 50:
        персонаж["Здоровье"] += 30
        персонаж["Мана"] -= 20
        print(f"{персонаж['Имя']} использует заклинание лечения! Здоровье восстановлено до: {персонаж['Здоровье']}, Мана: {персонаж['Мана']}")

while character1["Здоровье"] > 0 and character2["Здоровье"] > 0:

    урон1 = ударить_противника(character1, character2)
    character2["Здоровье"] -= урон1
    print(f"{character1['Имя']} атакует! {character2['Имя']} Здоровье: {character2['Здоровье']}")

    if character2["Здоровье"] <= 0:
        print(f"{character2['Имя']} побежден!")
        break

    урон2 = ударить_противника(character2, character1)
    character1["Здоровье"] -= урон2
    print(f"{character2['Имя']} атакует! {character1['Имя']} Здоровье: {character1['Здоровье']}")

    if character1["Здоровье"] <= 0:
        print(f"{character1['Имя']} побежден!")
        break
    использовать_зелье(character1)
    использовать_зелье(character2)
    использовать_заклинание_лечения(character1)
    использовать_заклинание_лечения(character2)

print(f"Здоровье {character1['Имя']}: {character1['Здоровье']}")
print(f"Здоровье {character2['Имя']}: {character2['Здоровье']}")
